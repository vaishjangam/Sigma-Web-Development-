/* Q2. Popcorn Price */
let size = 'XL'

if(size === 'XL'){
    console.log(`The price of popcorn is Rs.250`);
}else if(size === 'L'){
    console.log(`The price of popcorn is Rs.200`);
}else if(size === 'M'){
    console.log(`The price of popcorn is Rs.100`);
}else if(size === 'S'){
    console.log(`The price of popcorn is Rs.150`);
}else{
    console.log(`Enter correct prize`);
}
